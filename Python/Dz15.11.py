def divide_nuts(x, y):
    whole_nuts = y // x
    nuts_left = y % x

    print(f"Кожна білка отримає {whole_nuts} цілих горіхів.")
    print(f"Залишиться {nuts_left} горіхів.")

x = int(input("Введіть кількість білок (x): "))
y = int(input("Введіть кількість горіхів (y): "))
divide_nuts(x, y)

